package mx.gob.tabasco.saf.siafe.presupuesto.controladores.F2B;

public class CatalogoPeticionF2B {
	private String descripcion;
	private String modulo;
	private Long cvePadre;
	private Long cve;
	private String url;
	
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setModulo(String modulo) {
		this.modulo = modulo;
	}
	public String getModulo() {
		return modulo;
	}
	public void setCvePadre(Long cvePadre) {
		this.cvePadre = cvePadre;
	}
	public Long getCvePadre() {
		return cvePadre;
	}
	public void setCve(Long cve) {
		this.cve = cve;
	}
	public Long getCve() {
		return cve;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getUrl() {
		return url;
	}

}
